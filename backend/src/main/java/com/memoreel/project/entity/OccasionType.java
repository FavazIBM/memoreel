package com.memoreel.project.entity;

/**
 * Enum representing different types of occasions for memory reels.
 * 
 * This enum defines the various categories of events and celebrations
 * that users can create memory reels for.
 */
public enum OccasionType {
    /**
     * Birthday celebration
     */
    BIRTHDAY,
    
    /**
     * Anniversary celebration (wedding, relationship, etc.)
     */
    ANNIVERSARY,
    
    /**
     * Memorial or tribute for a deceased person
     */
    MEMORIAL,
    
    /**
     * Wedding celebration
     */
    WEDDING,
    
    /**
     * Graduation ceremony
     */
    GRADUATION,
    
    /**
     * Friendship celebration
     */
    FRIENDSHIP,
    
    /**
     * Travel memories
     */
    TRAVEL,
    
    /**
     * Custom occasion type
     */
    CUSTOM
}

