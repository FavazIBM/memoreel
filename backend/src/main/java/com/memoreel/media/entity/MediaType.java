package com.memoreel.media.entity;

/**
 * Enum representing the type of media file.
 * 
 * Used to distinguish between images and videos in the media upload system.
 */
public enum MediaType {
    /**
     * Image file (JPEG, PNG, etc.)
     */
    IMAGE,
    
    /**
     * Video file (MP4, MOV, etc.)
     */
    VIDEO
}

