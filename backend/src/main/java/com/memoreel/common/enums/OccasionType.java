package com.memoreel.common.enums;

/**
 * Enum representing different occasion types for memory reels.
 * 
 * Categories:
 * - Celebration: birthday, anniversary, wedding, graduation, baby_shower, housewarming, retirement
 * - Social: farewell, trip_memory, friendship, reunion, milestone, achievement
 * - Emotional (Memorial): memorial, condolence, remembrance_day
 * - Custom: custom
 * 
 * @author Memoreel Team
 */
public enum OccasionType {
    // Celebration Category
    BIRTHDAY,
    ANNIVERSARY,
    WEDDING,
    GRADUATION,
    BABY_SHOWER,
    HOUSEWARMING,
    RETIREMENT,
    
    // Social Category
    FAREWELL,
    TRIP_MEMORY,
    FRIENDSHIP,
    REUNION,
    MILESTONE,
    ACHIEVEMENT,
    
    // Emotional (Memorial) Category
    MEMORIAL,
    CONDOLENCE,
    REMEMBRANCE_DAY,
    
    // Custom Category
    CUSTOM
}

